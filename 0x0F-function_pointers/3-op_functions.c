#include "3-calc.h"

/**
 * op_add - a function that adds two integers.
 *
 * @a: int
 * @b: int
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - a function that subtracts two integers.
 *
 * @a: int
 * @b: int
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - a function that multiplies two integers.
 *
 * @a: int
 * @b: int
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - a function that divides two integers.
 *
 * @a: int
 * @b: int
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - a function that finds  modulus two integers.
 *
 * @a: int
 * @b: int
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
